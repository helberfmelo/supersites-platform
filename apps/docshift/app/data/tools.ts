function makeTool(spec: DocShiftToolSpec): DocShiftToolDefinition {
  return {
    slug: spec.slug,
    category: spec.category,
    operation: spec.operation,
    requiresPdf: spec.requiresPdf,
    acceptsMultiple: spec.acceptsMultiple,
    usesTextInput: spec.usesTextInput,
    defaultPages: spec.defaultPages,
    localized: Object.fromEntries(
      publicLocaleCodes.map((locale) => [locale, copyFor(spec, locale)]),
    ) as Record<LocaleCode, DocShiftToolCopy>,
  }
}

