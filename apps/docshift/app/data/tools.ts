function baseName(value: string): string {
  return value
    .replace(/\.[a-z0-9]+$/i, '')
    .toLowerCase()
    .replace(/[^a-z0-9-]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '') || 'docshift-document'
}

export function formatBytes(bytes: number): string {
  if (bytes < 1024) {
    return `${bytes} B`
  }

  if (bytes < 1024 * 1024) {
    return `${(bytes / 1024).toFixed(1)} KB`
  }

  return `${(bytes / (1024 * 1024)).toFixed(2)} MB`
}

function normalizeFileNames(value: string[]): string[] {
  return value.map((item) => item.trim()).filter(Boolean)
}

function hasPdfExtension(fileName: string): boolean {
  return /\.pdf$/i.test(fileName)
}

function validatePdfInputs(tool: DocShiftToolDefinition, input: DocShiftToolInput): void {
  if (!tool.requiresPdf) {
    return
  }

  const fileNames = normalizeFileNames(input.fileNames)
  if (fileNames.length === 0) {
    throw new Error('Choose a PDF file before processing.')
  }

  if (!tool.acceptsMultiple && fileNames.length > 1) {
    throw new Error('This free tool accepts one PDF at a time.')
  }

  if (fileNames.length > maxFreeFiles) {
    throw new Error(`Free DocShift merge accepts at most ${maxFreeFiles} PDFs.`)
  }

  const totalBytes = input.sizeBytes.reduce((sum, value) => sum + value, 0)
  if (totalBytes <= 0 || totalBytes > maxFreeBytes) {
    throw new Error(`Free DocShift files must total at most ${formatBytes(maxFreeBytes)}.`)
  }

  for (let index = 0; index < fileNames.length; index += 1) {
    const mimeType = (input.mimeTypes[index] ?? '').toLowerCase()
    if (!allowedPdfMimeTypes.has(mimeType) && !hasPdfExtension(fileNames[index])) {
      throw new Error('DocShift accepts PDF files only in the free document MVP.')
    }
  }
}

function normalizeRotation(value: unknown): 90 | 180 | 270 {
  const parsed = Number(value ?? 90)
  return parsed === 180 || parsed === 270 ? parsed : 90
}

function sanitizeShortText(value: unknown, fallback: string, limit: number): string {
  const normalized = String(value ?? '').replace(/\s+/g, ' ').trim()
  if (!normalized) {
    return fallback
  }

  return normalized.slice(0, limit)
}

export function parsePageSelection(value: string | undefined, pageCount: number): number[] {
  if (!Number.isInteger(pageCount) || pageCount <= 0 || pageCount > maxPageNumber) {
    return []
  }

  const source = (value ?? '').trim().toLowerCase()
  if (!source || source === 'all') {
    return Array.from({ length: pageCount }, (_, index) => index)
  }

  const selected = new Set<number>()
  for (const part of source.split(',')) {
    const token = part.trim()
    if (!token) {
      continue
    }

    const range = token.match(/^(\d+)\s*-\s*(\d+)$/)
    if (range) {
      const start = Number(range[1])
      const end = Number(range[2])
      const min = Math.min(start, end)
      const max = Math.max(start, end)
      for (let page = min; page <= max; page += 1) {
        if (page >= 1 && page <= pageCount) {
          selected.add(page - 1)
        }
      }
      continue
    }

    const page = Number(token)
    if (Number.isInteger(page) && page >= 1 && page <= pageCount) {
      selected.add(page - 1)
    }
  }

  return [...selected].sort((a, b) => a - b)
}

export function estimateOutputBytesForPlan(plan: DocShiftTransformPlan, input: DocShiftToolInput): number {
  const totalBytes = input.sizeBytes.reduce((sum, value) => sum + value, 0)
  if (plan.operation === 'text-to-pdf') {
    return Math.max(1_800, Math.round(plan.textContent.length * 8))
  }

  if (plan.operation === 'split' && input.pageCount && plan.pageSelection.length > 0) {
    return Math.max(1_200, Math.round(totalBytes * (plan.pageSelection.length / input.pageCount)))
  }

  if (plan.operation === 'compress' || plan.operation === 'metadata') {
    return Math.max(1_200, Math.round(totalBytes * 0.92))
  }

  return Math.max(1_200, totalBytes || 1_200)
}

function outputNameFor(tool: DocShiftToolDefinition, input: DocShiftToolInput): string {
  if (tool.operation === 'text-to-pdf') {
    return 'docshift-text-to-pdf.pdf'
  }

  const name = baseName(normalizeFileNames(input.fileNames)[0] ?? 'document')
  return `${name}-${tool.slug}.pdf`
}

export function planDocShiftTransform(slug: DocShiftToolSlug, input: DocShiftToolInput, workerUsed = false): DocShiftToolResult {
  try {
    const tool = getDocShiftToolBySlug(slug)
    if (!tool) {
      return fail('Tool not found.')
    }

    validatePdfInputs(tool, input)

    const pageCount = Math.min(Math.max(Number(input.pageCount ?? 1), 1), maxPageNumber)
    const pageSelection = parsePageSelection(input.pageSelection ?? tool.defaultPages, pageCount)
    const warnings: string[] = []
    const textContent = String(input.textContent ?? '').trim()

    if (tool.operation === 'text-to-pdf') {
      if (textContent.length === 0) {
        throw new Error('Paste plain text before generating a PDF.')
      }

      if (textContent.length > maxTextCharacters) {
        throw new Error(`Text to PDF is limited to ${maxTextCharacters.toLocaleString('en-US')} characters in the free MVP.`)
      }
    }

    if (tool.operation === 'split' && pageSelection.length === 0) {
      throw new Error('Choose at least one valid page number or range.')
    }

    if (tool.operation === 'merge' && normalizeFileNames(input.fileNames).length === 1) {
      warnings.push('Only one PDF is selected. Add more files to combine documents.')
    }

    if (tool.operation === 'compress') {
      warnings.push('Browser-side compression is a safe structure rewrite, not image downsampling or OCR compression.')
    }

    if (tool.operation === 'metadata') {
      warnings.push('Metadata cleanup is not forensic redaction. Visible page content is not removed.')
    }

    const plan: DocShiftTransformPlan = {
      slug,
      operation: tool.operation,
      outputName: outputNameFor(tool, input),
      pageSelection,
      rotationDegrees: normalizeRotation(input.rotationDegrees),
      watermarkText: sanitizeShortText(input.watermarkText, 'Draft', 64),
      metadataTitle: sanitizeShortText(input.metadataTitle, 'DocShift output', 90),
      metadataAuthor: sanitizeShortText(input.metadataAuthor, 'DocShift local tool', 90),
      textContent,
      workerUsed,
      warnings,
      privacyNote: 'Processed locally in this browser session; no upload endpoint, localStorage or sessionStorage is used.',
      upgradeGateNote: 'Batch jobs, larger files, OCR, table extraction, history, API, teams and server queues remain gated.',
    }

    return {
      ok: true,
      output: plan.outputName,
      meta: [
        { label: 'Input files', value: String(tool.operation === 'text-to-pdf' ? 0 : normalizeFileNames(input.fileNames).length) },
        { label: 'Input total', value: formatBytes(input.sizeBytes.reduce((sum, value) => sum + value, 0)) },
        { label: 'Pages planned', value: tool.operation === 'text-to-pdf' ? 'new PDF' : String(plan.pageSelection.length || pageCount) },
        { label: 'Estimated output', value: formatBytes(estimateOutputBytesForPlan(plan, input)) },
      ],
      plan,
    }
  } catch (error) {
    return fail(error instanceof Error ? error.message : 'Document plan failed.')
  }
}
