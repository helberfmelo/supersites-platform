export function getDocShiftToolBySlug(slug: string | undefined): DocShiftToolDefinition | null {
  if (!docShiftToolSlugs.includes(slug as DocShiftToolSlug)) {
    return null
  }

  return toolBySlug.get(slug as DocShiftToolSlug) ?? null
}

export function getDocShiftToolCopy(tool: DocShiftToolDefinition, locale: LocaleCode): DocShiftToolCopy {
  return sanitizePublicCopy(locale, tool.localized[locale])
}

export function getRelatedDocShiftTools(slug: DocShiftToolSlug, locale: LocaleCode): DocShiftRelatedTool[] {
  return relatedBySlug[slug]
    .map((relatedSlug) => getDocShiftToolBySlug(relatedSlug))
    .filter((tool): tool is DocShiftToolDefinition => Boolean(tool))
    .map((tool) => {
      const copy = getDocShiftToolCopy(tool, locale)

      return {
        slug: tool.slug,
        title: copy.title,
        description: copy.freeScope,
      }
    })
}

export function getDocShiftWorkflowSteps(slug: DocShiftToolSlug, locale: LocaleCode = 'en'): DocShiftWorkflowStep[] {
  return sanitizePublicCopy(locale, workflowStepsBySlug[slug])
}

export function getCategoryLabel(category: DocShiftToolCategory, locale: LocaleCode): string {
  return categoryLabels[category][locale]
}

export function filterDocShiftTools(query: string, category: DocShiftToolCategory | 'all', locale: LocaleCode): DocShiftToolDefinition[] {
  const normalizedQuery = query.trim().toLowerCase()

  return docShiftToolCatalog.filter((tool) => {
    const copy = getDocShiftToolCopy(tool, locale)
    const matchesCategory = category === 'all' || tool.category === category
    const searchableText = [
      tool.slug,
      tool.operation,
      getCategoryLabel(tool.category, locale),
      copy.title,
      copy.shortName,
      copy.headline,
      copy.description,
      copy.freeScope,
      copy.upgradeScope,
    ].join(' ').toLowerCase()

    return matchesCategory && (!normalizedQuery || searchableText.includes(normalizedQuery))
  })
}

